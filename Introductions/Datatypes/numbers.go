package main

import (
  "fmt"
)

func main() {
    i := 100
    var j int = 1234 // int allows for the representation of positive and negative integer values.
    fmt.Printf("%v + %v = %v\n", i, j, i+j)

    f := 1.5 // a float type because we are using a decimal point and a number
    fmt.Printf("f = %v\n", f)
}
